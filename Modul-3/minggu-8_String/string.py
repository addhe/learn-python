# Contoh string
nama = "John Doe"
print(nama)

# Menggabungkan string
nama_depan = "Jane"
nama_belakang = "Doe"
nama_lengkap = nama_depan + " " + nama_belakang
print(nama_lengkap)

# Memotong string
kalimat = "Ini adalah contoh kalimat"
print(kalimat[0:3])
print(kalimat[4:])
print(kalimat[:3])

# Metode string
print(nama_lengkap.upper())
print(nama_lengkap.lower())
print(nama_lengkap.capitalize())
