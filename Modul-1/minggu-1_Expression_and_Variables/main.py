# Contoh Penggunaan Variabel (Kotak Ajaib)

# Menyimpan nama buah di variabel buah_kesukaan
buah_kesukaan = "apel"
print("Isi kotak buah_kesukaan adalah:", buah_kesukaan)

# Menyimpan jumlah kelereng di variabel jumlah_kelereng
jumlah_kelereng = 5
print("Isi kotak jumlah_kelereng adalah:", jumlah_kelereng)

# Kamu bisa mengganti isi kotak kapan saja!
jumlah_kelereng = 10
print("Sekarang isi kotak jumlah_kelereng adalah:", jumlah_kelereng)

print("\n------------------------------------\n")

# Contoh Penggunaan Ekspresi (Bermain dengan Isi Kotak)

# Menghitung total kelereng
kelereng_awal = 5
kelereng_tambahan = 2
total_kelereng = kelereng_awal + kelereng_tambahan # Ini adalah ekspresi penjumlahan
print("Kelereng awal:", kelereng_awal)
print("Dapat tambahan:", kelereng_tambahan)
print("Total kelereng sekarang:", total_kelereng)

print("\n------------------------------------\n")

# Menggabungkan kata-kata (string)
kata_salam = "Halo "
nama_panggilan = "Jagoan"
kalimat_sapaan = kata_salam + nama_panggilan + "!" # Ini adalah ekspresi penggabungan string
print(kalimat_sapaan)

kata_awal = "Aku suka "
buah = "pisang" # Kita ganti buahnya dari contoh cerita
kalimat_lengkap = kata_awal + buah
print(kalimat_lengkap)

print("\n------------------------------------\n")
print("Selamat mencoba bermain dengan variabel dan ekspresi sendiri!")